        Container(
          width: columnWidths[2],
          child: _buildHeaderCell(context, 'Phone'),
        ),

        // Email
        Container(
          width: columnWidths[3],
          child: _buildHeaderCell(context, 'Email'),
        ),

        // Last Purchase
        Container(
          width: columnWidths[4],
          child: _buildHeaderCell(context, 'Last Purchase'),
        ),

        // Customer Since
        Container(
          width: columnWidths[5],
          child: _buildHeaderCell(context, 'Customer Since'),
        ),

        // Actions
        Container(
          width: columnWidths[6],
          child: _buildHeaderCell(context, 'Actions'),
        ),
      ],
    );
  }

  List<double> _getColumnWidths(BuildContext context) {
    return [
      120.0, // Customer ID
      200.0, // Name
      180.0, // Phone
      250.0, // Email
      150.0, // Last Purchase
      150.0, // Customer Since
      320.0, // Actions - increased width for View button
    ];
  }

